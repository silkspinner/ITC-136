# First Linux command line primer
pwd     # Command to Print Working Directory, (Where)
whoami  # Command to return user ID, (Who)
ls      # Command to show contents of directory, (What)
        # -l switch displays information about contents of directory
cd      # Command to change directory, (Navigation)
date    # Command to show machine time, (When)


